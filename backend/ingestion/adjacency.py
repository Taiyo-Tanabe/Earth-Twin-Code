def build_adjacency() -> pd.DataFrame:
    """
    隣国リストをlong形式のDataFrameに変換して保存。
    country_code, neighbor_code の2列。
    """
    rows = []
    for country, neighbors in ADJACENCY.items():
        for neighbor in neighbors:
            rows.append({"country_code": country, "neighbor_code": neighbor})

    df = pd.DataFrame(rows)
    dest = PROCESSED_PATH / "adjacency.csv"
    save_csv(df, dest)
    logger.info(f"Adjacency list saved: {len(df)} pairs, {df['country_code'].nunique()} countries → {dest}")
    return df
